''' Write a Python program to perform basic arithmetic operations 
(addition, subtraction, multiplication, division, and modulus) on two numbers.
Prompt the user to enter two numbers.
Perform the arithmetic operations and print the results. 
(Use appropriate operators and print formatting for clear output.) '''

num1 = float(input("Enter the first number "))
num2 = float(input ("Enter the second number "))
print("Addition of two numbers",num1 + num2)
print("Subtraction of two numbers",num1 - num2)
print("Multiplication of two numbers",num1 * num2)
print("Division of two numbers",num1 / num2)
print("Modulus of two numbers",num1 % num2)
